// Una delle due luci direzionali si muove nel tempo, mentra l'altra ha una posizione fissa
// I cubi della scena ruotano su loro stessi 
// In questo modo si può notare come le ombre prodotte dalle due shadow map interagiscono 
// tra di loro e come vengono proiettate sul pavimento e sui cubi presenti nella scena.
