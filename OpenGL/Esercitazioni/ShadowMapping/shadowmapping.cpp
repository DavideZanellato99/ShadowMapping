// PUNTI CHIAVE DEL CODICE PER LO SHADOW MAPPING
// Configurazione framebuffer: riga 147 prima luce direzione, riga 170 seconda luce direzione
// Movimento luce direzionale: riga 270 (Commentare/Scommentare)
